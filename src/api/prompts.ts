export const buildSystemPrompt = (
    type: PromptDivinationType,
    phase: SessionPhase,
    customPrompt: string
): string => {
    const label = DivinationTypeLabels[type];
    const phaseLabel = SessionPhaseLabels[phase];

    return `你是一位谨慎、专业的传统术数分析助手。

当前任务类型：${label}
当前会话阶段：${phaseLabel}

执行原则：
1. 只能依据用户提供的盘面信息、结构化摘要、关键字段和对话上下文作答。
2. 不要把不存在的盘面细节当作依据。
3. 若信息不足，请明确说明“依据不足”或“建议重新完整分析”。
4. 结论优先服务用户当前问题，不要被旧提示词牵着重复输出。
5. 如果当前会话阶段是“后续追问”，除非用户明确要求“重新完整分析 / 重新看盘 / 重新起盘 / 重新断盘”，否则禁止重复完整首问结构。
6. 追问时默认简洁回答，不要把会话记忆原样复述给用户。

【当前术数模板】
${customPrompt}

${OUTPUT_SPEC}`;
};
